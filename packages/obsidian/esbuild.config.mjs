const syncToObsidian = () => {
	if (obsidianPluginPath) {
		// æ£€æŸ¥ç›®æ ‡ç›®å½•æ˜¯å¦å­˜åœ¨ï¼Œä¸å­˜åœ¨åˆ™åˆ›å»º
		if (!existsSync(obsidianPluginPath)) {
			const shortPath = obsidianPluginPath.replace(process.env.HOME, "~");
			console.log(`ğŸ“ Creating plugin directory: ${shortPath}`);
			try {
				mkdirSync(obsidianPluginPath, { recursive: true });
				console.log(`âœ… Directory created`);
			} catch (error) {
				console.error(
					`âŒ Failed to create directory: ${error.message}`,
				);
				process.exit(1);
			}
		}

		try {
			execSync(
				`rsync -a -q packages/obsidian/dist/ "${obsidianPluginPath}"`,
				{
					stdio: "pipe",
					cwd: path.resolve("../.."),
				},
			);
			console.log(`âœ… Synced to plugin path: ${obsidianPluginPath}`);
		} catch (error) {
			console.error("âŒ Sync failed:", error.message);
			process.exit(1);
		}
	}
};

